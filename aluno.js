class Aluno {
    nome;
    curso;
    ano;
    periodo;
    especializacao;
    media;

    constructor(nome, curso, ano, periodo, especializacao, media){
        this.nome = nome;
        this.curso = curso;
        this.ano = ano;
        this.periodo = periodo;
        this.especializacao = especializacao;
        this.media = media;
    }
}